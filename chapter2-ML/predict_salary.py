"""
개발자 연봉 예측 - 모델 추론 샘플 코드 (수강생용)

streamlit 없이 학습된 모델(salary_model.pkl)을 불러와 예측만 해봅니다.
이 코드를 참고해서 여러분만의 streamlit 앱을 만들어 보세요!

실행:  python predict_salary.py
"""

import pickle

import pandas as pd

# ------------------------------------------------------------
# 1. 모델 불러오기 - 학습 때 저장한 pickle 파일을 그대로 로드
# ------------------------------------------------------------
with open("salary_model.pkl", "rb") as f:
    model = pickle.load(f)

# ------------------------------------------------------------
# 2. 입력(Feature) 준비 - 예측하고 싶은 사람의 정보를 넣습니다
#    아래 값을 자유롭게 바꿔가며 결과를 확인해 보세요.
#    (학습 때 쓴 컬럼 이름/카테고리와 똑같이 맞춰야 합니다)
# ------------------------------------------------------------
input_df = pd.DataFrame([{
    "years_experience": 3,              # 경력 연수 (0~25)
    "job_role": "백엔드",                # 직군
    "main_language": "Python",          # 주 사용 언어
    "education": "학사",                 # 최종 학력 (고졸/학사/석사/박사)
    "company_size": "중소기업 (50~300명)",  # 회사 규모
}])

# ------------------------------------------------------------
# 3. 예측 - model.predict()가 핵심! 결과는 배열이라 [0]으로 꺼냅니다
# ------------------------------------------------------------
predicted_salary = model.predict(input_df)[0]

# ------------------------------------------------------------
# 4. 출력 - CLI라 print로 간단하게
# ------------------------------------------------------------
print("입력 정보")
print("-" * 30)
for column, value in input_df.iloc[0].items():
    print(f"  {column}: {value}")
print("-" * 30)
print(f"예상 연봉: {predicted_salary:,.0f} 만원")
